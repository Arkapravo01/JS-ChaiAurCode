const month="feb"
switch(month){
    case 1:
        console.log("January")
        break
    case "feb":
        console.log("February")
        break
    case 3:
        console.log("March")
        break
    case 4:
        console.log("April")
        break
    case "5":
        console.log("May")
        break
    case 6:
        console.log("June")
        break
    default:
        console.log("Default case match")
        break
}